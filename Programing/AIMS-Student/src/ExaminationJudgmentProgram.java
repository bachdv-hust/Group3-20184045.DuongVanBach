public class ExaminationJudgmentProgram {
    private float math;
    private float physics;

    public ExaminationJudgmentProgram(float math, float physics) {
        this.math = math;
        this.physics = physics;
    }

    public float getMath() {
        return math;
    }

    public void setMath(float math) {
        this.math = math;
    }

    public float getPhysics() {
        return physics;
    }

    public void setPhysics(float physics) {
        this.physics = physics;
    }
}
